#!/bin/bash

##########################################################################################################

set -e

##########################################################################################################

_gather-environment 'HOME'

##########################################################################################################

: "${WISH_CSSO_DOCKER_IMAGE:=elnebuloso/csso-cli}"
: "${WISH_CSSO_DOCKER_IMAGE_TAG:=latest}"
: "${WISH_CSSO_DOCKER_IMAGE_CMD:=}"

##########################################################################################################

_docker-pull-image "${WISH_CSSO_DOCKER_IMAGE}:${WISH_CSSO_DOCKER_IMAGE_TAG}"

##########################################################################################################

run="docker run --tty --interactive --rm --user $(id -u) --workdir $(pwd) --volume $(pwd):$(pwd) --volume /var/run/docker.sock:/var/run/docker.sock --env-file /tmp/env ${WISH_CSSO_DOCKER_IMAGE}:${WISH_CSSO_DOCKER_IMAGE_TAG} ${WISH_CSSO_DOCKER_IMAGE_CMD} $@"

##########################################################################################################

_docker-run-image "${run}"

##########################################################################################################
#!/bin/bash

##########################################################################################################

ARGS=""
VERSION=$(cat /VERSION)

##########################################################################################################

while [[ "$#" -gt 0 ]]
do
  case $1 in
    -v|--version)
      echo "wish ${VERSION}"
      exit 0;
      ;;
    -h|--help)
      echo "#"
      echo "# wish ${VERSION} help"
      echo "#"
      exit 0;
      ;;
    *)
      ARGS="${ARGS}${1} "
  esac
  shift
done

##########################################################################################################